var a;

console.log(a);

if (a === undefined) {
	console.log('a is undefined');
} else {
	console.log('a is defined');
}

/* 
Undefined é um valor para uma variável declarada cujo valor não foi atribuído.
É um valor que existe e ocupa espaço em memória.
Não é o mesmo que uma variável inexistente (Uncaught Reference).
*/
